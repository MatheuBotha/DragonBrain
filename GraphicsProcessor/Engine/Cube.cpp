void Cube::activateShader()
{
    shaderProgram.use();
}

void Cube::deactivateShader()
{
    shaderProgram.unuse();
}

void Cube::rotate(GLfloat angle, glm::vec3 rotationVector)
{
    model = glm::rotate(model, angle, rotationVector);
}

void Cube::scale(glm::vec3 scaleVector)
{
    model = glm::scale(model, scaleVector);
}

void Cube::translate(glm::vec3 location)
{
    model = glm::translate(model, location);
}

void Cube::setModel()
{
    model = glm::mat4();
}

void Cube::setCamera(Camera* camera)
{
    this->camera = camera;
}